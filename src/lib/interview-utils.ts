/**
 * 将对话历史转换为 LLM 可读的文本格式
 */
export function formatDialogueForLLM(history: ChatMessage[]): string {
  return history
    .map((msg) => {
      const speaker = msg.role === "assistant" ? "采访者" : "受访者";
      return `[${speaker}]：${msg.content}`;
    })
    .join("\n\n");
}

